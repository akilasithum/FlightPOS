package com.pos.flightpos.objects;

public class FullFlight {

    private String flightId;
    private String obFlightName;
    private String obFrom;
    private String obTo;
    private String ibFlightName;
    private String ibFrom;
    private String ibTo;

    public String getFlightId() {
        return flightId;
    }

    public void setFlightId(String flightId) {
        this.flightId = flightId;
    }

    public String getObFlightName() {
        return obFlightName;
    }

    public void setObFlightName(String obFlightName) {
        this.obFlightName = obFlightName;
    }

    public String getObFrom() {
        return obFrom;
    }

    public void setObFrom(String obFrom) {
        this.obFrom = obFrom;
    }

    public String getObTo() {
        return obTo;
    }

    public void setObTo(String obTo) {
        this.obTo = obTo;
    }

    public String getIbFlightName() {
        return ibFlightName;
    }

    public void setIbFlightName(String ibFlightName) {
        this.ibFlightName = ibFlightName;
    }

    public String getIbFrom() {
        return ibFrom;
    }

    public void setIbFrom(String ibFrom) {
        this.ibFrom = ibFrom;
    }

    public String getIbTo() {
        return ibTo;
    }

    public void setIbTo(String ibTo) {
        this.ibTo = ibTo;
    }
}
